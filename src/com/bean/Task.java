package com.bean;

public class Task {
	private String projectid;//项目编号
	
	private String name;//项目名称
	
	private String early;//前期
	
	private String earperson;//前期人员
	
	private String middle;//中期
	
	private String midperson;//中期人员
	
	private String late;//后期
	
	private String lateperson;//后期人员
	
	private String leader;//项目负责人
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	

	public String getEarly() {
		return early;
	}

	public void setEarly(String early) {
		this.early = early;
	}

	public String getEarperson() {
		return earperson;
	}

	public void setEarperson(String earperson) {
		this.earperson = earperson;
	}

	public String getMiddle() {
		return middle;
	}

	public void setMiddle(String middle) {
		this.middle = middle;
	}

	public String getMidperson() {
		return midperson;
	}

	public void setMidperson(String midperson) {
		this.midperson = midperson;
	}

	public String getLate() {
		return late;
	}

	public void setLate(String late) {
		this.late = late;
	}

	public String getLateperson() {
		return lateperson;
	}

	public void setLateperson(String lateperson) {
		this.lateperson = lateperson;
	}

	public String getLeader() {
		return leader;
	}

	public void setLeader(String leader) {
		this.leader = leader;
	}

	public String getProjectid() {
		return projectid;
	}

	public void setProjectid(String projectid) {
		this.projectid = projectid;
	}

	
}
